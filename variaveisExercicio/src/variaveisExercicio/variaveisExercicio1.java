package variaveisExercicio;

public class variaveisExercicio1 {

	public static void main(String[] args) {
		int num1;
		int num2;
		char caractere1;
		char caractere2;
		String texto1;
		String texto2;
		boolean verdadeiro;
		boolean falso;

		num1 = 100;
		num2 = 200;
		caractere1 = 'c';
		caractere2 = '@';
		texto1 = "Gustavo";
		texto2 = "Messias";
		verdadeiro = true;
		falso = false;

		System.out.println(num1);
		System.out.println(num2);
		System.out.println(texto1);
		System.out.println(texto2);
		System.out.println(caractere1);
		System.out.println(caractere2);
		System.out.println(verdadeiro);
		System.out.println(falso);

	}

}
